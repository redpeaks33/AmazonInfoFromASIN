        private void ButtonClick_GetProductInfo(object sender, EventArgs e)
        {
            //Need Refactoring

            var ASINList = CreateASINList();

            #region fetchProduct Info

            sw.Start();
            var productInfoList = fetchProductInfo.Start(ASINList, AmazonIDList);
            sw.Stop();
            Console.WriteLine("Fetch Product Info = {0}", sw.Elapsed);
            sw.Reset();

            #endregion fetchProduct Info

            #region Create Table

            sw.Start();
            productInfoTableCreator.Start(this.productInfoDataGridView, productInfoList);
            sw.Stop();
            Console.WriteLine("Create Table = {0}", sw.Elapsed);
            sw.Reset();

            #endregion Create Table
        }

        private string[] CreateASINList()
        {
            var strList = this.richTextBox.Text.Replace("\r", "_").Replace("\n", "_").Split('_');

            return strList;
        }

        private void ButtonClick_ExportToExcel(object sender, EventArgs e)
        {
            var fileDialog = new SaveFileDialog()
                {
                    //InitialDirectory = System.Environment.GetFolderPath(Environment.SpecialFolder.DesktopDirectory),
                    InitialDirectory = Environment.SpecialFolder.Personal.ToString(),
                    Filter = "CSV File (.csv)|*.csv|All Files (*.*)|*.*",
                    FileName = "AmazonPoductInfo.csv",
                    FilterIndex = 1,
                };
            try
            {
                if (fileDialog.ShowDialog() == DialogResult.OK)
                {
                    sw.Reset();
                    sw.Start();
                    excelExporter.Start(this.productInfoDataGridView, fileDialog.FileName);
                    sw.Stop();
                    Console.WriteLine("Export To Excel Total = {0}", sw.Elapsed);
                }
                MessageBox.Show("Completed");
            }
            catch (Exception exception)
            {
                MessageBox.Show("Error Happened : " + exception.Message);
            }
        }

